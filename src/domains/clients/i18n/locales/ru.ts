function getClientSuffix(count: number): string {
  const mod10 = count % 10
  const mod100 = count % 100
  if (mod100 >= 11 && mod100 <= 19) return 'ов'
  if (mod10 === 1) return 'а'
  if (mod10 >= 2 && mod10 <= 4) return 'а'
  return 'ов'
}
